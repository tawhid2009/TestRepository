package dataTypes;

public class JavaOperators {

	public static void main(String[] args) {
		
		//Arithmetic operators, + - * / ++(increment) --(decrement).{++, -- We use them in Loop}.
		
				int a = 10;
				int b = 5;
				int result;
				
				result = a + b;//addition
				System.out.println("Addition result is " + result);//15
				result = a - b;
				System.out.println("Subtraction result is " + result);//5
				result = a * b;
				System.out.println("Multiplication result is " + result);//50
				result = a / b;
				System.out.println("Division result is " + result);//2
				result = a % b;
				System.out.println("Moduls result is " + result);//0

	}

}
